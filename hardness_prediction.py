# ==========================================
stacking = StackingRegressor(
estimators=[
('rf', rf),
('gb', gb),
('svm', svm),
('knn', knn),
('ridge_base', Ridge(alpha=1.0))
],
final_estimator=Ridge(alpha=1.0),
cv=10,
n_jobs=-1
)

