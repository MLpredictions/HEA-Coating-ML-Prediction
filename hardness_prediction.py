#ANN based sensitivity analysis_Hardness

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.model_selection import train_test_split
import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, Input

# ==========================================
# 2. Load Dataset
# ==========================================
file_path = '/content/Hardness data_Original.csv'
df = pd.read_csv(file_path)


df = df.drop(columns=['HEA combinations'], errors='ignore')

if 'Phases' in df.columns:
    df['Phases'] = df['Phases'].astype('category').cat.codes

# ==========================================
# 3. SELECT FEATURES
# ==========================================
features = ['VEC','ΔHmix','ΔSmix','Δχ','Tm','Ω','δ','Al','Fe','Co','Cr','Ni','Mn','Cu','Si','Ti','V','W','Mo','Nb','Zr','Hf','B','Ta']
features = [f for f in features if f in df.columns]

X = df[features]
y = df['Hardness']

# ==========================================
# 4. Normalize
# ==========================================
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Train-test split (needed for the ANN training for meaningful sensitivity)
X_train, X_test, y_train, y_test = train_test_split(
X_scaled, y, test_size=0.2, random_state=42
)

# ==========================================
# 5. ANN Model for Regression
# (predicting Hardness, not classification)
# ==========================================
def build_ann_regression_model(input_shape):
    model = Sequential([
        Input(shape=(input_shape,)), # Recommended way to specify input shape
        Dense(64, activation='relu'),
        Dense(32, activation='relu'),
        Dense(1, activation='linear') # Single output for regression, linear activation
    ])
    model.compile(optimizer='adam',
                  loss='mean_squared_error') # MSE loss for regression
    return model

# ==========================================
# 6. Gradient-Based Sensitivity Calculation
# This calculates d(Hardness_prediction) / d(Input_Feature_j)
# ==========================================

runs = 5 # Number of model re-initializations and training runs to average sensitivity
S_runs = [] # To store sensitivity vectors from each run

for r in range(runs):
    model = build_ann_regression_model(X.shape[1])
    model.fit(X_train, y_train, epochs=100, verbose=0) # Train on training data

    # Convert full scaled data to TensorFlow tensor for gradient computation
    X_tensor = tf.convert_to_tensor(X_scaled, dtype=tf.float32)

    with tf.GradientTape() as tape:
        tape.watch(X_tensor)
        preds = model(X_tensor)

    # Gradients of the output (preds) with respect to the input (X_tensor)
    grads = tape.gradient(preds, X_tensor)

    if grads is not None:
        # Average gradients over all samples to get a single sensitivity vector for this run
        S_runs.append(np.mean(grads.numpy(), axis=0))
    else:
        S_runs.append(np.zeros(X.shape[1])) # Append zeros if gradients are None

S_runs = np.array(S_runs) # Shape: (runs, n_features)

features_names = X.columns.tolist() # Get feature names from the original DataFrame

# ==========================================
# 7. Sort Features by Global Importance
# ==========================================
# Calculate global importance by averaging absolute sensitivity across runs
global_importance = np.mean(np.abs(S_runs), axis=0)
sorted_idx = np.argsort(global_importance)[::-1]

features_sorted = [features_names[i] for i in sorted_idx]
S_runs_sorted = S_runs[:, sorted_idx]

# ==========================================
# 8. Determine Global Min/Max for Consistent Scaling
# ==========================================
global_ymin = np.min(S_runs_sorted)
global_ymax = np.max(S_runs_sorted)

# Adjust for symmetry around 0 and add buffer
abs_max = np.max(np.abs([global_ymin, global_ymax]))
global_ymin = -abs_max * 1.1 # Add 10% buffer
global_ymax = abs_max * 1.1 # Add 10% buffer

# ==========================================
# 9. Radar Plot Function for Regression Sensitivity
# ==========================================
def plot_regression_radar(ax, values, features, title, global_ymin, global_ymax, color):
    angles = np.linspace(0, 2*np.pi, len(features), endpoint=False)

    # Close loop by repeating the first value and angle
    values_closed = np.concatenate((values, [values[0]]))
    angles_closed = np.concatenate((angles, [angles[0]]))

    ax.plot(angles_closed, values_closed, linewidth=1.5, alpha=0.6, color=color)
    ax.fill(angles_closed, values_closed, alpha=0.1, color=color)

    ax.set_xticks(angles) # Set ticks for each feature
    ax.set_xticklabels(features, fontsize=9, weight='bold') # Use bold for emphasis

    ax.set_ylim(global_ymin, global_ymax) # Use global min/max for consistent scaling
    ax.set_title(title, fontsize=12, weight='bold', pad=20)

    # Set grid and labels for the radial axis
    ax.set_yticks(np.linspace(global_ymin, global_ymax, 5))
    ax.tick_params(axis='y', labelsize=10)
    ax.grid(True, linestyle='--', alpha=0.7)

# ==========================================
# 10. Plot Average Sensitivity
# ==========================================
fig, ax = plt.subplots(figsize=(7, 6), subplot_kw=dict(polar=True))

avg_sensitivity = np.mean(S_runs_sorted, axis=0)

plot_regression_radar(
    ax,
    avg_sensitivity,
    features_sorted,
    "Average Hardness Prediction Sensitivity (ANN)",
    global_ymin,
    global_ymax,
    'blue' # Single color for the average
)

plt.suptitle('ANN Gradient-Based Sensitivity for Hardness Prediction', fontsize=14, weight='bold', y=1.02)
plt.tight_layout(rect=[0, 0, 1, 0.98]) # Adjust layout to make space for suptitle
plt.savefig('ann_hardness_sensitivity_radar.png', dpi=300)
plt.show()
plt.close(fig) # Close the figure to free memory


print("✅ ANN gradient-based sensitivity analysis complete and plot saved!")