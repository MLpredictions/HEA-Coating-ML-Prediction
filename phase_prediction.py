#ANN based sentivity analysis_Phase

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.model_selection import train_test_split
import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense

# ==========================================
# 2. Load Dataset
# ==========================================
file_path = 'phase dataset_original.csv'
df = pd.read_csv(file_path)

df = df.drop(columns=['HEA combinations', 'Unnamed: 23'], errors='ignore') # Dropping 'Unnamed: 23' here

# ==========================================
# 3. Features & Target
# ==========================================
X = df.select_dtypes(include=[np.number])
y_raw = df['Phases']

le = LabelEncoder()
y = le.fit_transform(y_raw)
class_names = le.classes_

# ==========================================
# 4. Normalize
# ==========================================
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Train-test split (needed for the ANN training for meaningful sensitivity)
X_train, X_test, y_train, y_test = train_test_split(
X_scaled, y, test_size=0.2, random_state=42
)

# ==========================================
# 5. ANN Model
# ==========================================
def build_model():
    model = Sequential([
        Dense(32, activation='relu', input_shape=(X.shape[1],)),
        Dense(16, activation='relu'),
        Dense(len(class_names), activation='softmax')
    ])
    model.compile(optimizer='adam',
                  loss='sparse_categorical_crossentropy',
                  metrics=['accuracy'])
    return model

# ==========================================
# 6. Sensitivity Matrix (Gradient-Based)
# The sensitivity matrix for an Artificial Neural Network (ANN) measures how much
# the output of each class changes with respect to a small change in each input feature.
# It's calculated by taking the derivative (gradient) of each output class probability
# with respect to each input feature. A larger absolute value indicates higher sensitivity.
#
# In this context, we calculate d(Output_Class_i)/d(Input_Feature_j).
# We average these gradients across all samples to get a robust measure.
# This helps understand which features are most influential for predicting each phase.
# ==========================================

runs = 5 # Number of model re-initializations and training runs to average sensitivity
S_runs = [] # To store sensitivity matrices from each run

for r in range(runs):
    model = build_model()
    model.fit(X_train, y_train, epochs=100, verbose=0) # Train on training data

    X_tensor = tf.convert_to_tensor(X_scaled, dtype=tf.float32) # Use full scaled data for sensitivity

    grads_list = []
    for i in range(len(class_names)):
        with tf.GradientTape() as tape:
            tape.watch(X_tensor)
            preds = model(X_tensor)[:, i]
            grads = tape.gradient(preds, X_tensor)
        # Handle cases where gradients might be None (e.g., dead ReLU or very small contributions)
        if grads is not None:
            grads_list.append(grads.numpy())
        else:
            grads_list.append(np.zeros_like(X_scaled)) # Ensures we always have a numpy array, even if all grads are None

    grads_combined = np.stack(grads_list, axis=-1) # Shape: (n_samples, n_features, n_classes)

    # Average over samples to get S for this run (shape: n_features, n_classes)
    S_runs.append(np.mean(grads_combined, axis=0))

S_runs = np.array(S_runs) # Shape: (runs, n_features, n_classes)

features = X.columns.tolist() # Get feature names from the original DataFrame

# ==========================================
# 7. Sort Features by Global Importance (Optional, for clearer plots)
# ==========================================
# Calculate global importance by averaging absolute sensitivity across runs and classes
global_importance = np.mean(np.abs(S_runs), axis=(0, 2))
sorted_idx = np.argsort(global_importance)[::-1]

features_sorted = [features[i] for i in sorted_idx]
S_runs_sorted = S_runs[:, sorted_idx, :]

# ==========================================
# 8. Radar Plot Function (Figure 10 Style)
# ==========================================
def plot_radar(ax, values, features, title, global_ymin, global_ymax, color_idx):
    angles = np.linspace(0, 2*np.pi, len(features), endpoint=False)

    # Close loop by repeating the first value and angle
    values_closed = np.concatenate((values, [values[0]]))
    angles_closed = np.concatenate((angles, [angles[0]]))

    # Define a color palette for different runs if needed, or use a single color
    colors = plt.cm.viridis(np.linspace(0, 1, runs))

    ax.plot(angles_closed, values_closed, linewidth=1.5, alpha=0.6, color=colors[color_idx % runs])
    ax.fill(angles_closed, values_closed, alpha=0.1, color=colors[color_idx % runs])

    ax.set_xticks(angles) # Set ticks for each feature
    ax.set_xticklabels(features, fontsize=9, weight='bold') # Use bold for emphasis

    ax.set_ylim(global_ymin, global_ymax) # Use global min/max for consistent scaling
    ax.set_title(title, fontsize=12, weight='bold', pad=20)

    # Set grid and labels for the radial axis
    ax.set_yticks(np.linspace(global_ymin, global_ymax, 5))
    ax.tick_params(axis='y', labelsize=10) # Increased labelsize for radial axis
    ax.grid(True, linestyle='--', alpha=0.7)

# ==========================================
# 9. Determine Global Min/Max for Consistent Scaling
# ==========================================
global_ymin = np.min(S_runs_sorted)
global_ymax = np.max(S_runs_sorted)

# Adjust for symmetry around 0
abs_max = np.max(np.abs([global_ymin, global_ymax]))
global_ymin = -abs_max
global_ymax = abs_max

# Add a small buffer to the limits for better visualization
buffer = abs_max * 0.1
global_ymin -= buffer
global_ymax += buffer


# ==========================================
# 10. Plot All Phases Individually and Save
# ==========================================

for i, phase_name in enumerate(class_names):
    fig, ax = plt.subplots(figsize=(7, 6), subplot_kw=dict(polar=True))
    for r_idx in range(runs):
        plot_radar(
            ax,
            S_runs_sorted[r_idx, :, i], # Sensitivity for this run, sorted features, and class
            features_sorted,
            f"{phase_name} Phase", # Removed (a), (b), etc. prefix
            global_ymin,
            global_ymax,
            r_idx
        )
    plt.suptitle('Feature Sensitivity (ANN Gradient-Based)', fontsize=14, weight='bold', y=1.02)
    plt.tight_layout(rect=[0, 0, 1, 0.98]) # Adjust layout to make space for suptitle
    plt.savefig(f'sensitivity_radar_plot_{phase_name.replace(" ", "_")}.png', dpi=300)
    plt.show()
    plt.close(fig) # Close the figure to free memory